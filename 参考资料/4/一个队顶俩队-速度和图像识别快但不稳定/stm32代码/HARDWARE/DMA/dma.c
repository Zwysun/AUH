void avr_filter(void)
{
	int sum = 0;
	u8 count;
	for(i=0;i<2;i++)
	{
		for (count=0;count<10;count++)
		{
			sum += AD_Value[count][i];
		}
	AD_Fin[i]=sum/10;
	sum=0;
	}
}	 























